/**
 * 定义和用法: sin() 方法可返回一个数字的正弦。
 * 语法: Math.sin(x); x	必需。一个以弧度表示的角。将角度乘以 0.017453293 （2PI/360）即可转换为弧度。
 * 返回值: 参数 x 的正弦值。返回值在 -1.0 到 1.0 之间。
 * 说明:
 * 注释:
 */

console.log(Math.sin(3)); // 0.1411200080598672
console.log(Math.sin(-3)); // -0.1411200080598672
console.log(Math.sin(0)); // 0
console.log(Math.sin(Math.PI)); // 1.2246467991473532e-16
console.log(Math.sin(Math.PI/2)); // 1